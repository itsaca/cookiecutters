using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class SpawnerManager : MonoBehaviour
{
    public float timer;
    public float time = 2;
    

    void Start()
    {
        
    }

    void Update()
    {
        //spawning system here
        while (timer < time) {
            timer += Time.deltaTime;
            if {timer > time}
            timer -= time;
        }

    }
}
